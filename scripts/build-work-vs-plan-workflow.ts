// Assembles the ce-verify-work dynamic workflow into a single self-contained
// script. The Workflow runtime cannot import sibling files and requires
// `export const meta` as the first statement, so the canonical, unit-tested
// deterministic module (drift-rollup.js) is inlined into the fan-out template
// (work-vs-plan-fanout.js) at its merge-module marker.
//
// The guard in SKILL.md reads the GENERATED output and passes it to the Workflow
// tool verbatim — no runtime text-surgery. Regenerate after editing either
// source:
//
//   bun run scripts/build-work-vs-plan-workflow.ts
//
// A freshness test (tests/work-vs-plan-workflow-parity.test.ts) fails if the
// committed generated file drifts from the sources.

import { readFile, writeFile } from "fs/promises"
import path from "path"

const WF_DIR = "plugins/compound-engineering/skills/ce-verify-work/workflows"
const MARKER = "/* __MERGE_MODULE__ */"
export const GENERATED_PATH = `${WF_DIR}/work-vs-plan-fanout.generated.js`

const GENERATED_HEADER =
  "// GENERATED by scripts/build-work-vs-plan-workflow.ts — DO NOT EDIT.\n" +
  "// Edit work-vs-plan-fanout.js (fan-out + roll-up) or drift-rollup.js (parser + roll-up), then run:\n" +
  "//   bun run scripts/build-work-vs-plan-workflow.ts\n\n"

/** Produce the assembled workflow script (with the generated header). */
export async function assembleWorkVsPlanWorkflow(root: string = process.cwd()): Promise<string> {
  const moduleSrc = await readFile(path.join(root, `${WF_DIR}/drift-rollup.js`), "utf8")
  const fanout = await readFile(path.join(root, `${WF_DIR}/work-vs-plan-fanout.js`), "utf8")

  // Drop the module's trailing `export { ... };` so the inlined source is valid
  // inside the export-less workflow script. Function replacement keeps `$` in
  // the inserted source (template literals) literal.
  const moduleInline = moduleSrc.replace(/\nexport\s*\{[^}]*\};\s*$/, "\n")
  if (moduleInline === moduleSrc) {
    throw new Error("drift-rollup.js: expected a trailing `export { ... };` to strip")
  }
  if ((fanout.match(/\/\* __MERGE_MODULE__ \*\//g) || []).length !== 1) {
    throw new Error("work-vs-plan-fanout.js: merge-module marker must appear exactly once")
  }

  return GENERATED_HEADER + fanout.replace(MARKER, () => moduleInline)
}

if (import.meta.main) {
  const out = await assembleWorkVsPlanWorkflow()
  await writeFile(path.join(process.cwd(), GENERATED_PATH), out, "utf8")
  console.log(`Wrote ${GENERATED_PATH} (${out.length} bytes)`)
}
